// Operators
//
// 1. Arithmetic Operators
//
let a = 5;
let b = 10;

console.log("Let be a = 5 and b = 10: ");
console.log(" a + b = ", a + b);
console.log(" a - b = ", a - b);
console.log(" a * b = ", a * b);
console.log(" a / b = ", a / b);
console.log(" a % b = ", a % b);
console.log(" a ** b = ", a ** b);
a++;
console.log(" a++ =  ", a);
b--;
console.log(" b-- = ", b);

